<p>Bienvenido a LibroExpress.org</p>
<p>Es muy grato poder contar su visita a nuestro portal de  venta de libros para todas las edades.</p>
<p>El procedimiento para comprar es muy sencillo:</p>
<ol>
  <li>Seleccione la categor&iacute;a principal con la que  identifica el libro que desea. (Men&uacute; en la parte superior)<p><img src="../imagenes/Botones superiores/menusuperiorcompleto.png" width="904" height="50" /></p>
  </li>
  <li>Escoja la categor&iacute;a espec&iacute;fica del tema en el  que est&aacute; interesado. (Men&uacute; en la parte derecha)<p><img src="../imagenes/Botones derechos/carrito-02.png" width="101" height="40" /></p>
  </li>
  <li>Dele clic al enlace que dice &ldquo;<img src="../imagenes/Imagenes de edicion/agregar.png" width="15" height="15" />Agregar al carrito&rdquo;,  el cual inmediatamente pondr&aacute; su libro a en un carrito virtual por si tiene m&aacute;s  libros de su preferencia que desea comprar.</li>
  <li>De clic en el men&uacute; superior que dice &ldquo;carrito de  compra&rdquo;.<p><img src="../imagenes/Botones superiores/carrito-02.png" width="125" height="50" /></p>
  </li>
  <li>En el men&uacute; derecho de clic sobre el vinculo que  dice &ldquo;Ver mi carrito&rdquo;. En esta secci&oacute;n podr&aacute; ver todo lo que ha agregado a su  carrito de la compra. Si por alguna raz&oacute;n no aparece de clic sobre el v&iacute;nculo  que dice &ldquo;Actualizar el carrito&rdquo;.</li>
<p>Si usted desea agregar m&aacute;s unidades de un libro determinado, ingrese la  cantidad en el formulario y de clic sobre el icono actualizar al lado derecho o  oprima la tecla &ldquo;Enter&rdquo; de su teclado. (Solo se actualiza un libro a la vez).</p>
  <li>Si est&aacute; conforme con su pedido de clic en el  vinculo del men&uacute; derecho que dice &ldquo;Confirmar pedido&rdquo;</li>
  <li>Llene los datos correctos y de clic sobre el  bot&oacute;n que dice &ldquo;<img src="../imagenes/Imagenes de edicion/continuar.png" width="100" height="25" align="absmiddle" />&rdquo;</li>
  <li>Inmediatamente su factura pro-forma le ser&aacute; enviada  al correo que usted ha registrado.</li>
  <li>En ella aparece el n&uacute;mero de cuenta de nuestro  banco donde podr&aacute; realizar la consignaci&oacute;n correspondiente al valor de la  compra para que su pedido sea enviado.</li>
  <li>Despu&eacute;s envi&eacute; por correo electr&oacute;nico a gerencia@libroexpress.org,  o por fax la copia de la consignaci&oacute;n para proceder con el envi&oacute; de su pedido.</li>
  <li>Su pedido, dependiendo del lugar donde resida, estar&aacute;  llegando entre 1 y 3 d&iacute;as h&aacute;biles.</li>
</ol>
